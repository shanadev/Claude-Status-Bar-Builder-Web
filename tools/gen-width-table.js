// Derives src/StatusBar.Core/UnicodeWidth.cs from the widths reported by the
// bundled xterm unicode11 addon, so Composer's alignment math and the preview
// terminal can never disagree about how many cells a glyph occupies.
// Rerun after any xterm addon upgrade:  node tools/gen-width-table.js

const path = require('path');
const fs = require('fs');

const root = path.join(__dirname, '..');
const addonPath = path.join(root, 'src', 'StatusBar.Web', 'wwwroot', 'xterm', 'addon-unicode11.js');
const outPath = path.join(root, 'src', 'StatusBar.Core', 'UnicodeWidth.cs');

const A = require(addonPath);
const addon = new A.Unicode11Addon();
let provider = null;
addon.activate({ unicode: { register(p) { provider = p; } } });
if (!provider || provider.version !== '11') throw new Error('unicode11 provider not captured');

// Sweep every codepoint, collapse equal-width neighbors into [start, end] ranges.
// Surrogates are skipped: System.Text.Rune can never hold one.
const zero = [], wide = [];
for (let cp = 0; cp <= 0x10FFFF; cp++) {
  if (cp >= 0xD800 && cp <= 0xDFFF) continue;
  const w = provider.wcwidth(cp);
  if (w === 1) continue;
  const list = w === 0 ? zero : w === 2 ? wide : null;
  if (!list) throw new Error('unexpected width ' + w + ' at U+' + cp.toString(16));
  const last = list[list.length - 1];
  if (last && cp === last[1] + 1) last[1] = cp; else list.push([cp, cp]);
}

// The C# fast path assumes printable ASCII is 1 cell — verify against the provider.
for (let cp = 0x20; cp <= 0x7E; cp++) {
  if (provider.wcwidth(cp) !== 1) throw new Error('ASCII fast path violated at U+' + cp.toString(16));
}

function csTable(list) {
  const flat = [];
  for (const [a, b] of list) flat.push(a, b);
  const lines = [];
  for (let i = 0; i < flat.length; i += 10) {
    lines.push('        ' + flat.slice(i, i + 10)
      .map(v => '0x' + v.toString(16).toUpperCase().padStart(4, '0'))
      .join(', ') + ',');
  }
  return lines.join('\n');
}

const cs = `// Claude Status Bar Builder — a visual designer for Claude Code status lines.
// Copyright (C) 2026 Stephen Shanafelt
// SPDX-License-Identifier: GPL-3.0-only
//
// This program is free software: you can redistribute it and/or modify it under
// the terms of the GNU General Public License as published by the Free Software
// Foundation, version 3. It is distributed WITHOUT ANY WARRANTY; see the full
// license text in the LICENSE file at the root of this repository.
//
// GENERATED by tools/gen-width-table.js from the widths reported by the bundled
// @xterm/addon-unicode11 (src/StatusBar.Web/wwwroot/xterm/addon-unicode11.js).
// ${zero.length} zero-width ranges, ${wide.length} wide ranges. Do not edit the
// tables by hand — rerun the generator after any xterm addon upgrade.

namespace StatusBar.Core;

/// <summary>
/// Display-cell width per codepoint, matching the preview terminal's Unicode 11
/// provider exactly: flex-spacer and right-alignment math must agree with what
/// xterm.js actually paints, cell for cell. Modern terminals (Windows Terminal,
/// iTerm2, kitty) use the same widths for everything a status line plausibly holds.
/// </summary>
public static class UnicodeWidth
{
    /// <summary>Cells the codepoint occupies: 0 (combining/format), 1, or 2.</summary>
    public static int Of(int codepoint)
    {
        if (codepoint >= 0x20 && codepoint <= 0x7E) return 1; // printable ASCII fast path
        if (InTable(Zero, codepoint)) return 0;
        return InTable(Wide, codepoint) ? 2 : 1;
    }

    static bool InTable(int[] pairs, int cp)
    {
        int lo = 0, hi = pairs.Length / 2 - 1;
        while (lo <= hi)
        {
            int mid = (lo + hi) / 2;
            if (cp < pairs[mid * 2]) hi = mid - 1;
            else if (cp > pairs[mid * 2 + 1]) lo = mid + 1;
            else return true;
        }
        return false;
    }

    // [start, end] pairs, flattened, ascending.
    static readonly int[] Zero =
    {
${csTable(zero)}
    };

    static readonly int[] Wide =
    {
${csTable(wide)}
    };
}
`;

fs.writeFileSync(outPath, cs, { encoding: 'utf8' }); // LF, no BOM — repo convention

const check = (cp) => provider.wcwidth(cp);
console.log('wrote ' + outPath);
console.log('ranges: zero=' + zero.length + ' wide=' + wide.length);
console.log('spot: U+23F0=' + check(0x23F0) + ' U+1F33F=' + check(0x1F33F) +
  ' U+E0B0=' + check(0xE0B0) + ' U+FE0F=' + check(0xFE0F) + ' U+4E00=' + check(0x4E00));
